fruta = input("Digite a fruta: ").upper()
if fruta == 'BANANA':
    print("O preço da Banana é R$ 5,23")
elif fruta == 'Maça':
    print("O preço da Maça é R$ 12,10")
elif fruta == 'Cereja':
    print("O preço da Cereja é R$ 58,00")
else:
    print("Desculpe-nos. A fruta ", fruta, ", está em falta!")